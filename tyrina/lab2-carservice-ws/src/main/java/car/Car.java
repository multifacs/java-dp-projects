package car;

public class Car {
    public int id;
    public String model;
    public int mileage;
    public String condition;
    public int cost;

    public Car(int id, String model, int mileage, String condition, int cost) {
        this.id = id;
        this.model = model;
        this.mileage = mileage;
        this.condition = condition;
        this.cost = cost;
    }
}
